// The main function - tune the receiver and wait for a lock
BOOL TuneReceiver(PSOURCESTRUCT ss, char * szTunerString)
{
	int nLBand;
	int nFEC = 9;		// default to auto
	int nMode = 0;		// default to DVB QPSK
	int nRFInput = 1;	// default to antenna 1
	int n4800Polarity;
	int nLNBVoltage;
	DWORD dwTimeoutTime = 0;
	BOOL fRetVal = FALSE;
	char szCommand[256];
	char szCRC[16];

	if (ss->nFrequency > ss->nLNBFrequency)
		nLBand = ss->nFrequency - ss->nLNBFrequency;
	else
		nLBand = ss->nLNBFrequency - ss->nFrequency;

	// Translate TSReader's mode and FEC flags
	switch(ss->nADVModulationMode)
	{
	case ADV_MOD_DVB_QPSK:
		{
			// 1/2, 2/3, 3/4, 5/6, 7/8, Auto
			int bRateTable[] = {0, 1, 2, 5, 7, 9};
			nFEC = bRateTable[ss->nCodeRate];
			nMode = 0;
		}
		break;
	case ADV_MOD_DCII_C_QPSK:
	case ADV_MOD_DCII_I_QPSK:
	case ADV_MOD_DCII_Q_QPSK:
	case ADV_MOD_DCII_C_OQPSK:
		{
			// 5/11, 1/2, 3/5, 2/3, 3/4, 4/5, 5/6, 7/8, Auto
			int bRateTable[] = {8, 0, 3, 1, 2, 4, 5, 7, 9};
			nFEC = bRateTable[ss->nCodeRate];
			if (ss->nADVModulationMode == ADV_MOD_DCII_I_QPSK)
				nMode = 2;
			else if (ss->nADVModulationMode == ADV_MOD_DCII_Q_QPSK)
				nMode = 3;
			else if (ss->nADVModulationMode == ADV_MOD_DCII_C_OQPSK)
				nMode = 5;
			else
				nMode = 1;		// DCII QPSK combo
		}
		break;
	case ADV_MOD_TURBO_QPSK:
	case ADV_MOD_TURBO_8PSK:
	case ADV_MOD_TURBO_16QAM:
		MessageBox(ss->hWndTSReader, "The currently selected receiver doesn't support this modulation mode", gszAppName, MB_ICONSTOP);
		return FALSE;
	}

	// Translate DiSEqC options
	if (ss->nDiSEqCInput > 0 && ss->nDiSEqCInput < 4)
		nRFInput = ss->nDiSEqCInput;
	
	// Power/Tone all all that stuff
	switch(ss->nPolarity)
	{
	case 1:		// H
		n4800Polarity = 0;
		nLNBVoltage = 2;
		break;
	case 0:		// V
		n4800Polarity = 1;
		nLNBVoltage = 1;
		break;
	default:	// power off
		n4800Polarity = 0;
		nLNBVoltage = 0;
		break;
	}

	wsprintf(szCommand, "%c%04X%04X%02X %02X:%01X:%04X:%01X:%01X:%c",
			 2,			// stx
			 0x1000,	// slave_address
			 0x0101,	// command_code = modify table
			 0x04,		// table = 4
			 0,			// satellite ID
			 n4800Polarity,
			 ss->nLNBFrequency, 
			 nLNBVoltage,
			 ss->n22KHz,
			 3);		// etx
	wsprintf(szCRC, "%04X", CRC16(szCommand, lstrlen(szCommand)));
	lstrcat(szCommand, szCRC);
	SourceHelper_SendReceiverSerial(szCommand, lstrlen(szCommand));
	Read4800Response();

	// Do the tuner
	nLBand = (nLBand - 950) * 8;

	// Send tuner command
	wsprintf(szCommand, "%c%04X%04X%02X%01X:%04X:%04X:%01X:%01X:%c",
			 2,			// stx
			 0x1000,	// slave_address
			 0x0101,	// command_code = modify table
			 0x05,		// table = 5
			 nRFInput,
			 nLBand,
			 ss->nSymbolRate,
			 nFEC,
			 nMode,
			 3);		// etx
	wsprintf(szCRC, "%04X", CRC16(szCommand, lstrlen(szCommand)));
	lstrcat(szCommand, szCRC);
	SourceHelper_SendReceiverSerial(szCommand, lstrlen(szCommand));
	Read4800Response();

	// Wait a lock
	Sleep(2000);
	dwTimeoutTime = GetTickCount() + (60 * 1000);
	do
	{
		if (Get4800TunerStatus() == TRUE)
		{
			fRetVal = TRUE;
			break;
		}
		Sleep(100);
	} while (GetTickCount() < dwTimeoutTime);
	if (!fRetVal)
	{
		if (ss->fQuietMode == FALSE)
		{
			MessageBox(ss->hWndTSReader, "Failed to lock signal", gszAppName, MB_ICONWARNING);
			return FALSE;
		}
	}			

	return TRUE;
}

// This function is called when the TSReader user selects a channel in the PMT tree
// It selects the channel on the receiver therefore enabling any CA authorization
// for that channel
BOOL SetChannel(int nSID, int nTSID, int nNID)
{
	char szCommand[128];
	char szCRC[16];

	wsprintf(szCommand, "%c%04X%04X%02X%04X:%c",
		     2,			// stx
			 0x1000,	// slave_address
			 0x0101,	// command_code = modify table
			 0x09,		// table = 9
			 nSID,
			 3);		// etx
	wsprintf(szCRC, "%04X", CRC16(szCommand, lstrlen(szCommand)));
	lstrcat(szCommand, szCRC);
	SourceHelper_SendReceiverSerial(szCommand, lstrlen(szCommand));
	Read4800Response();

	return TRUE;
}
