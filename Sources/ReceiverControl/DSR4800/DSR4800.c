unsigned short CRC16(char * message, int message_length)
{
	unsigned char CRC16_Hi_Byte = 0xff;
	unsigned char CRC16_Low_Byte = 0xff;
	unsigned short index;
	
	while( message_length > 0 ) 
	{
		index = CRC16_Hi_Byte ^*message;
		CRC16_Hi_Byte = CRC16_Low_Byte ^ CRCHI_Look_Up[index];
		CRC16_Low_Byte = CRCLO_Look_up[index];
		++message;
		--message_length;
	}
  	return (CRC16_Hi_Byte << 8 | CRC16_Low_Byte);
}

char * Read4800Response()
{
	BOOL fGotSTX = FALSE;
	char szCRC[5];
	int nOffsetPointer, nCRCOffset;
	BOOL fCRCPickup = FALSE;
	BYTE c;
	static BYTE szResponseBuffer[256];

	do
	{
		int nTimeout = 1000;
		c = SourceHelper_ReceiveReceiverSerial(&nTimeout);
		if (nTimeout == 0)
			break;

		if (fCRCPickup == TRUE)
		{
			szCRC[nCRCOffset++] = c;
			if (nCRCOffset == 4)
			{
				szCRC[nCRCOffset] = 0;
				break;
			}
		}
		else
		{
			if (fGotSTX == FALSE)
			{
				if (c == 2)
				{
					fGotSTX = TRUE;
					nOffsetPointer = 0;
				}
			}
			else
			{
				if (c == 3)
				{
					szResponseBuffer[nOffsetPointer] = 0;
					fCRCPickup = TRUE;
					nCRCOffset = 0;
				}
				else
				{
					szResponseBuffer[nOffsetPointer++] = c;
				}
			}		
		}
	} while (TRUE);

	return szResponseBuffer;
}

BYTE * ReadNewtecResponse()
{
	BOOL fFoundACK = FALSE;
	BOOL fWaitingCRC = FALSE;
	int nOffsetPointer = 0;
	int nNoDataCounter = 0;
	BYTE c;
	static BYTE szResponseBuffer[256];

	do
	{
		int nTimeout = 1000;
		c = SourceHelper_ReceiveReceiverSerial(&nTimeout);
		if (nTimeout == 0)
			break;

		if (fFoundACK == FALSE)
		{
			if (c == 6)
				fFoundACK = TRUE;
		}
		else
		{
			if (fWaitingCRC == TRUE)
			{
				szResponseBuffer[nOffsetPointer++] = 0;
				break;
			}
			else
			{
				if (c == 3)
					fWaitingCRC = TRUE;
				else
					szResponseBuffer[nOffsetPointer++] = c;
			}
		}
	} while (TRUE);

	return szResponseBuffer;
}

BOOL Get4800TunerStatus()
{
	char szCommand[50];
	char szCRC[5];
	char * szResponse;
	int nTunerLockStatus;
	BOOL fRetVal = FALSE;

	wsprintf(szCommand, "%c%04X%04X%02X%c",
		     2,			// stx
			 0x1000,	// slave_address
			 0x0102,	// command_code = read table
			 0x06,		// table = 6
			 3);		// etx
	wsprintf(szCRC, "%04X", CRC16(szCommand, lstrlen(szCommand)));
	lstrcat(szCommand, szCRC);
	SourceHelper_SendReceiverSerial(szCommand, lstrlen(szCommand));
	
	// 10008102060685:2E:0000:0F:09:
	// 123456789.123456789.12345678.
	szResponse = Read4800Response();
	sscanf(&szResponse[23], "%x", &nTunerLockStatus);
	if (nTunerLockStatus == 0x0f)
		fRetVal = TRUE;

	return fRetVal;
}

