void SKL_FBB::Put_UEG(SKL_UINT32 v)              // unsigned
{
  if (!(v&~255)) Put_Bits(UEG_Tab[v][0],UEG_Tab[v][1]);
  else {
    v += 1;
    const int Len = SKL_BMASKS::Log2(v);
    //  Put_Bits(v, 2*Len-1);   <- this could overflow if 2*Len-1>24
    Put_Bits(0, Len-1);
    Put_Bits(v, Len  );
  }
}

void SKL_FBB::Put_SEG(SKL_INT32 v)               // signed
{
  if (!((v+128)&~255)) Put_Bits(SEG_Tab[v+128][0],SEG_Tab[v+128][1]);
  else {
    v = (v<0) ? 1-2*v : 2*v;
    const int Len = SKL_BMASKS::Log2(v);
    //  Put_Bits(v, 2*Len-1);   <- this could overflow if 2*Len-1>24
    Put_Bits(0, Len-1);
    Put_Bits(v, Len  );
  }
}

