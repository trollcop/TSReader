SKL_UINT32 SKL_FBB::UEG()              // unsigned
{
  Check_Bits(24);
  SKL_UINT32 B = Bits;
  if (B>=(1U<<27)) {
    B >>= 32-9;
    Discard_Safe(EG_Len[B-16]);
    return UEG_Val[B-16];
  }
  int Len = 5;
  B <<= 5;
  while( !(B & 0xff000000) )
  {
    B <<= 8;
    Len += 8;
  }
  Len += SKL_BMASKS::Get_First_Bit_Set_8(B>>24);
  Discard_Safe(Len);
  return Get_Bits(Len+1) - 1;
}

SKL_INT32 SKL_FBB::SEG()               // signed
{
  Check_Bits(24);
  SKL_UINT32 B = Bits;
  if (B>=(1U<<27)) {
    B >>= 32-9;
    Discard_Safe(EG_Len[B-16]);
    return SEG_Val[B-16];
  }
  int Len = 5;
  B <<= 5;
  while( !(B & 0xff000000) )
  {
    B <<= 8;
    Len += 8;
  }
  Len += SKL_BMASKS::Get_First_Bit_Set_8(B>>24);
  Discard_Safe(Len);
  const SKL_INT32 Val = Get_Bits(Len+1);
  return (Val&1) ? -(Val>>1) : (Val>>1);
}

