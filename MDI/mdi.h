#ifndef __MDI_H__
#define __MDI_H__

/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
/**********                                                       **********/
/**********                   Local Definitions                   **********/
/**********                                                       **********/
/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
#define MDI_MAX_SAMPLES			512

#define MDI_USE_FLOAT			1

/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
/**********                                                       **********/
/**********                   Type Definitions                    **********/
/**********                                                       **********/
/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
typedef unsigned __int8			MDIUBYTE;
typedef unsigned __int16		MDIUSHORT;
typedef unsigned __int32		MDIULONG;
typedef signed __int32			MDILONG;
typedef unsigned __int64		MDITIME;
#if MDI_USE_FLOAT
typedef float					MDIFLOAT;
#else
typedef signed __int32			MDIFLOAT;
#endif

typedef struct tMDISTREAM
{
	MDITIME		lastTime;
	MDIULONG	lastPcr;
	MDIUSHORT	pcrPid;
	MDIULONG	vBuffer;
	MDIUSHORT	index;
	MDIUBYTE	mtspSize;
	MDIUBYTE	flags;
	MDIULONG	nBytes;
	MDIUSHORT	syncErrors;
	MDIUSHORT	ccErrors;
	MDIULONG	*timeArray;
	MDIULONG	*bytesArray;
	MDIUBYTE	*pidArray;
	MDIULONG	processedTime;
	MDIULONG	processedBytes;
	MDIFLOAT	frbCurrent;
	MDIFLOAT	frbMin;
	MDIFLOAT	frbMax;
} tMDISTREAM;

typedef struct tMDISAMPLE
{
	MDIULONG	vBuffer;
	MDIFLOAT	delayFactor;
	MDIULONG	lossCount;
	MDIULONG	bitrate;
} tMDISAMPLE;

/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
/**********                                                       **********/
/**********                     mdiStreamInit                     **********/
/**********                                                       **********/
/**********  This function allocates resources required for       **********/
/**********  monitoring the stream.                               **********/
/**********                                                       **********/
/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
tMDISTREAM *mdiStreamInit(unsigned int mtspSize);

/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
/**********                                                       **********/
/**********                     mdiStreamStop                     **********/
/**********                                                       **********/
/**********  Free resources allocated for monitoring this         **********/
/**********  stream.  The tMDISTREAM passed in becomes invalid    **********/
/**********  after this call.                                     **********/
/**********                                                       **********/
/**********                                                       **********/
/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
void mdiStreamStop(tMDISTREAM *pStream);

/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
/**********                                                       **********/
/**********                       mdiPacket                       **********/
/**********                                                       **********/
/**********  This function gets called for each packet of the     **********/
/**********  stream being tracked.                                **********/
/**********                                                       **********/
/**********  The accuracy of the delay factor measurement depends **********/
/**********  on the timestamp ts being as close as possible to    **********/
/**********  the actual arrival time of the packet.               **********/
/**********                                                       **********/
/**********  payloadBytes indicates the number of actual MTSP     **********/
/**********  bytes in the packet.                                 **********/
/**********                                                       **********/
/**********  pPayload should point to the sync byte of the first  **********/
/**********  MTSP in the packet.                                  **********/
/**********                                                       **********/
/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
void mdiPacket(MDITIME ts, void *pPayload, unsigned int nPayloadBytes, tMDISTREAM *pStream);

/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
/**********                                                       **********/
/**********                       mdiSample                       **********/
/**********                                                       **********/
/**********  Retrieve the results of one sample period and        **********/
/**********  begin the next sample period.                        **********/
/**********                                                       **********/
/**********  The timestamp ts should reflect the current time.    **********/
/**********                                                       **********/
/**********  pSample will be filled in with the sample results.   **********/
/**********                                                       **********/
/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
void mdiSample(MDITIME ts, tMDISTREAM *pStream, tMDISAMPLE *pSample);

#endif							/* __MDI_H__ */

