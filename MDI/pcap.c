static PCAP_FILE_HEADER fileHdr;
static int bSwapped;

/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
/**********                                                       **********/
/**********                      pcapValidate                     **********/
/**********                                                       **********/
/**********  Very simplistic validation of a PCAP file.  Simply   **********/
/**********  checks the PCAP header for a magic number.           **********/
/**********                                                       **********/
/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
int pcapValidate(FILE *pFile)
{
	if (sizeof(PCAP_FILE_HEADER) != fread((void *)&fileHdr, 1, sizeof(PCAP_FILE_HEADER), pFile))
		return 1;

	switch (fileHdr.MagicNumber)
	{
	case PCAP_MAGIC:
		bSwapped = 0;
		break;

	case PCAP_SWAPPED_MAGIC:
		bSwapped = 1;
		break;

	default:
		return 1;
	}

	return 0;
}

/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
/**********                                                       **********/
/**********                        pcapRead                       **********/
/**********                                                       **********/
/**********  Reads the next available record in a PCAP file.      **********/
/**********  Fills in the fields of the tFRAME structure and      **********/
/**********  returns 0 or 1 if EOF is reached.                    **********/
/**********                                                       **********/
/***************************************************************************/
/***************************************************************************/
/***************************************************************************/
int pcapRead(tFRAME *pFrame, FILE *pFile)
{
	PCAP_RECORD_HEADER recHdr;

	if (sizeof(PCAP_RECORD_HEADER) != fread((void *)&recHdr, 1, sizeof(PCAP_RECORD_HEADER), pFile))
		return 1;

	if (bSwapped)
	{
		recHdr.nIncludedLength = Swap32(recHdr.nIncludedLength);
		recHdr.nTimeSeconds = Swap32(recHdr.nTimeSeconds);
		recHdr.nTimeMicroSeconds = Swap32(recHdr.nTimeMicroSeconds);
	}

	pFrame->ts = ((uint64_t)recHdr.nTimeMicroSeconds * 1000)
			+ ((uint64_t)recHdr.nTimeSeconds * 1000000000);

	if (recHdr.nIncludedLength != fread((void *)pFrame->pBuffer, 1, recHdr.nIncludedLength, pFile))
		return 1;
	pFrame->nBytes = recHdr.nIncludedLength;

	return 0;
}

