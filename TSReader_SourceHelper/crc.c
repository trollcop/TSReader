void SourceHelper_CRC_Init(void)
{
	DWORD crc, poly;
	int i, j;

	poly = (DWORD)0xEDB88320L;
	for (i = 0; i < 256; i++)
	{
		crc = i;
		for (j = 8; j > 0; j--)
		{
			if (crc & 1) 
			{
				crc = (crc >> 1) ^ poly;
			}
			else
			{
				crc >>= 1;
			}
		}
		crcTable[i] = crc;
	}
}

DWORD SourceHelper_CRC_Make(BYTE *Memory, DWORD Length)
{
	DWORD theCrc;
	
	theCrc = 0xffffffffL;
	while(Length--)
	{
		theCrc = ((theCrc >> 8) & 0x00FFFFFFL) ^ crcTable[(theCrc ^* Memory++) & 0xFFL];
	}

	return theCrc;
}

DWORD SourceHelper_CRC_Running(BYTE byte, DWORD theCrc)
{
	return ((theCrc>>8) & 0x00FFFFFFL) ^ crcTable[ (theCrc ^ byte) & 0xFFL ];
}

BOOL SourceHelper_CRC_Check(BYTE * pU8section, DWORD U16length)
{
DWORD crc = 0xffffffff;

	U16length -= 4;
	while( U16length--)
	{
		crc = (crc << 8) ^ crc32tab[((crc >> 24) ^ *(pU8section++)) & 0xff];
	}

	U16length = 4;
	while( U16length--)
	{
		crc = (crc << 8) ^ crc32tab[((crc >> 24) ^ *(pU8section++)) & 0xff];
	}

	return (crc == 0);
}

